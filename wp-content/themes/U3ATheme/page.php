        <p>As Australia reached the end of the 20th century there was a growing recognition of the demoralising effects of social isolation, particularly within the older sector of the community. Because it is largely invisible, it is an issue that until recently has been given scant attention. Being lonely is not something that many people are prepared to admit to, doing something about it seldom captures the imagination of mainstream society.
            <br><br>
            One organisation that has been well aware of the impact of this situation is U3A, (the University of the Third Age), an international movement designed to provide low cost, lifelong learning opportunities for retired people in an informal atmosphere. Under one name or another, U3A now has several millions of members around the world – China’s Seniors Universities alone boast one million. China’s Seniors Universities operate as U3As based on the European model (that is, attached to Universities) whereas in Australia it is the Cambridge model that is used where each U3A is autonomous and self supporting.
            <br><br>
            In Australia around 38,500 (circa 1999) older people regularly participate. No prior qualifications are necessary and no degrees are awarded. Implicit in its philosophy is an understanding of the importance of the social interaction that it provides through courses and discussions involving both Tutors and students. With everyone involved in the U3A movement already in the older age group and, with all of the members being volunteers, many U3As traditionally have a difficult time concentrating their efforts on maintaining activities for their own membership.</p>

